class Cafe:

    def __init__(self, cafe_name, location_url, open_time, close_time, coffee_rating, wifi_rating, power):
        self.cafe = cafe_name
        self.location = location_url
        self.open = open_time
        self.close = close_time
        self.coffee = coffee_rating
        self.wifi = wifi_rating
        self.power = power